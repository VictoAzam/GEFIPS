from __future__ import annotations

from PyQt5.QtGui import QIcon, QPixmap
from PyQt5.QtCore import Qt


def svg_to_pixmap(svg_str: str, size: int = 24, color: str = "#111827") -> QPixmap:
	"""Converte SVG (string) em QPixmap."""
	svg_colored = svg_str.replace("currentColor", color)
	pixmap = QPixmap()
	pixmap.loadFromData(svg_colored.encode("utf-8"))
	return pixmap.scaledToWidth(size, Qt.SmoothTransformation)


